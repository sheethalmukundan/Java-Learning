package While;

public class WhileLoopPractice {

	public static void main(String[] args) {
		
		int a = 0;
		 while(a<=10) {
			 
			 System.out.println(a);
			 a++;
		 }
		 
		 System.out.println("Example for Do While loop....");
		 
		 int b = 0;
		 do {
			 
			 System.out.println(b);
			 b++;
			 
		 }while(b<=10);

	}

}
